#include<iostream>
using namespace std;
//Tipos de datos:

int main(){
	
	//TIPO DE DATO ENTERO:
	int x=5;
	//Salida de un entero en consola.
	cout<<"\n\t El valor de x es: "<<x;
	//Entrada de un entero en consola.
	int y;
	cout<<"\n\t Dame un valor entero: ";
	cin>>y;
	cout<<"\n\t El numero introducido es: "<<y;
	
	//TIPO DE DATO FLOTANTE:
	float monto=23.93;
	//Salida de un flotante en consola.
	cout<<"\n\t El valor del flotante es: "<<monto;
	
	return 0;
}

